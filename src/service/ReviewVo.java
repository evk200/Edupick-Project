package service;

public class ReviewVo extends InstituteVo{
	
	private int ridx;
	private String rcontent;
	private String rwriter;
	private String rwriteday;
	private String rdelyn;
	private int iidx;
	private int cidx;
	
	public int getRidx() {
		return ridx;
	}
	public void setRidx(int ridx) {
		this.ridx = ridx;
	}
	public String getRcontent() {
		return rcontent;
	}
	public void setRcontent(String rcontent) {
		this.rcontent = rcontent;
	}
	public String getRwriter() {
		return rwriter;
	}
	public void setRwriter(String rwriter) {
		this.rwriter = rwriter;
	}
	public String getRwriteday() {
		return rwriteday;
	}
	public void setRwriteday(String rwriteday) {
		this.rwriteday = rwriteday;
	}
	public String getRdelyn() {
		return rdelyn;
	}
	public void setRdelyn(String rdelyn) {
		this.rdelyn = rdelyn;
	}
	public int getIidx() {
		return iidx;
	}
	public void setIidx(int iidx) {
		this.iidx = iidx;
	}
	public int getCidx() {
		return cidx;
	}
	public void setCidx(int cidx) {
		this.cidx = cidx;
	}
	

}
